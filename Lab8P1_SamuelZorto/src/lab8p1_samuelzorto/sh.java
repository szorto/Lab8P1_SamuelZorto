package lab8p1_samuelzorto;

public class sh {
    public String nomb;
    public String nombreal;
    public String ciudad;
    public String nose;

    public sh(String nomb, String nombreal, String ciudad, String nose) {
        this.nomb = nomb;
        this.nombreal = nombreal;
        this.ciudad = ciudad;
        this.nose = nose;
    }

    public String getNomb() {
        return nomb;
    }

    public void setNomb(String nomb) {
        this.nomb = nomb;
    }

    public String getNombreal() {
        return nombreal;
    }

    public void setNombreal(String nombreal) {
        this.nombreal = nombreal;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getNose() {
        return nose;
    }

    public void setNose(String nose) {
        this.nose = nose;
    }
    
    
}
